#!/system/bin/sh
set -u

if [ -z "${ACHOST:-}" ]; then
    if [ -n "${ACHOST_BIN:-}" ]; then
        ACHOST="$(CDPATH= cd -- "$ACHOST_BIN/.." && pwd)"
    elif [ -n "${SCRIPT_DIR:-}" ]; then
        ACHOST="$(CDPATH= cd -- "$SCRIPT_DIR/.." && pwd)"
    else
        ACHOST="/data/adb/achost"
    fi
fi

ACHOST_BASE="${ACHOST_BASE:-}"
if [ -z "$ACHOST_BASE" ]; then
    if [ -x "$ACHOST/bin/achost-supervise" ] || [ -x "$ACHOST/bin/achost-runtime-core" ]; then
        ACHOST_BASE="$ACHOST"
    elif [ -d /data/adb/modules/achost-base/achost ]; then
        ACHOST_BASE=/data/adb/modules/achost-base/achost
    elif [ -d /data/adb/modules/achost-runtime/achost ]; then
        ACHOST_BASE=/data/adb/modules/achost-runtime/achost
    else
        ACHOST_BASE="$ACHOST"
    fi
fi
ACHOST_DOCKER_MODULE="${ACHOST_DOCKER_MODULE:-/data/adb/modules/achost-docker/achost}"
ACHOST_LXC_MODULE="${ACHOST_LXC_MODULE:-/data/adb/modules/achost-lxc/achost}"
ACHOST_COMMON="${ACHOST_COMMON:-$ACHOST_BASE}"
ACHOST_COMMON_BIN="${ACHOST_COMMON_BIN:-$ACHOST_COMMON/bin}"

ACHOST_RUNTIME_MODE_INPUT="${ACHOST_RUNTIME_MODE-}"
ACHOST_RUNTIME_MODE_WAS_SET="${ACHOST_RUNTIME_MODE+x}"
ACHOST_USE_CHROOT_INPUT="${ACHOST_USE_CHROOT-}"
ACHOST_USE_CHROOT_WAS_SET="${ACHOST_USE_CHROOT+x}"
ACHOST_CGROUP_MODE_INPUT="${ACHOST_CGROUP_MODE-}"
ACHOST_CGROUP_MODE_WAS_SET="${ACHOST_CGROUP_MODE+x}"

ACHOST_BIN="${ACHOST_BIN:-$ACHOST/bin}"
ACHOST_ETC="${ACHOST_ETC:-$ACHOST/etc}"
ACHOST_RUNTIME_CONF="${ACHOST_RUNTIME_CONF:-$ACHOST_ETC/achost-runtime.conf}"
[ ! -r "$ACHOST_RUNTIME_CONF" ] || . "$ACHOST_RUNTIME_CONF"
[ -z "$ACHOST_RUNTIME_MODE_WAS_SET" ] || ACHOST_RUNTIME_MODE="$ACHOST_RUNTIME_MODE_INPUT"
[ -z "$ACHOST_USE_CHROOT_WAS_SET" ] || ACHOST_USE_CHROOT="$ACHOST_USE_CHROOT_INPUT"
[ -z "$ACHOST_CGROUP_MODE_WAS_SET" ] || ACHOST_CGROUP_MODE="$ACHOST_CGROUP_MODE_INPUT"
ACHOST_RUNTIME_MODE="${ACHOST_RUNTIME_MODE:-native}"
case "$ACHOST_RUNTIME_MODE" in
    native) ACHOST_USE_CHROOT_DEFAULT=0 ;;
    chroot) ACHOST_USE_CHROOT_DEFAULT=1 ;;
    *) ACHOST_RUNTIME_MODE=native; ACHOST_USE_CHROOT_DEFAULT=0 ;;
esac
ACHOST_USE_CHROOT="${ACHOST_USE_CHROOT:-$ACHOST_USE_CHROOT_DEFAULT}"
ACHOST_CGROUP_MODE="${ACHOST_CGROUP_MODE:-v1}"
case "$ACHOST_CGROUP_MODE" in
    v1|v2) ;;
    *) ACHOST_CGROUP_MODE=v1 ;;
esac

ACHOST_VAR="${ACHOST_VAR:-$ACHOST/var}"
ACHOST_CONFIG="${ACHOST_CONFIG:-$ACHOST_VAR/config}"
ACHOST_BIND_PATHS="${ACHOST_BIND_PATHS:-/data/local/tmp $ACHOST_VAR/bind-mounts}"
ACHOST_RUN="${ACHOST_RUN:-$ACHOST_VAR/run}"
ACHOST_LOG_DIR="${ACHOST_LOG_DIR:-$ACHOST_VAR/log}"
if [ -d "$ACHOST/lxc" ]; then
    ACHOST_LXC="${ACHOST_LXC:-$ACHOST/lxc}"
elif [ -d "$ACHOST_LXC_MODULE/lxc" ]; then
    ACHOST_LXC="${ACHOST_LXC:-$ACHOST_LXC_MODULE/lxc}"
else
    ACHOST_LXC="${ACHOST_LXC:-$ACHOST/lxc}"
fi
ACHOST_LXC_BIN="${ACHOST_LXC_BIN:-$ACHOST_LXC/bin}"
ACHOST_LXC_ETC="${ACHOST_LXC_ETC:-$ACHOST/etc/lxc}"
ACHOST_LXC_VAR="${ACHOST_LXC_VAR:-/data/adb/achost/lxc}"
ACHOST_LXC_RUN="${ACHOST_LXC_RUN:-/data/adb/achost/run/lxc}"
ACHOST_LXC_LOG="${ACHOST_LXC_LOG:-/data/adb/achost/log/lxc}"
ACHOST_LXC_ROOTFS="${ACHOST_LXC_ROOTFS:-$ACHOST_LXC_VAR/rootfs}"
ACHOST_LXC_CONTAINERS="${ACHOST_LXC_CONTAINERS:-$ACHOST_LXC_VAR/containers}"
LXC_BRIDGE="${LXC_BRIDGE:-lxcbr0}"
LXC_SUBNET="${LXC_SUBNET:-172.32.0.0/16}"
ACHOST_CHROOT="${ACHOST_CHROOT:-$ACHOST_VAR/chroot}"
ACHOST_CHROOT_LAUNCH_MODE="${ACHOST_CHROOT_LAUNCH_MODE:-pivot}"
ACHOST_NATIVE_ROOT="${ACHOST_NATIVE_ROOT:-$ACHOST_VAR/native-root}"
ACHOST_DOCKER_ROOT="${ACHOST_DOCKER_ROOT:-$ACHOST_VAR/docker}"
ACHOST_DOCKER_EXEC_ROOT="${ACHOST_DOCKER_EXEC_ROOT:-$ACHOST_RUN/docker-exec}"
ACHOST_CONTAINERD_ROOT="${ACHOST_CONTAINERD_ROOT:-$ACHOST_VAR/containerd/root}"
ACHOST_CONTAINERD_STATE="${ACHOST_CONTAINERD_STATE:-$ACHOST_VAR/containerd/state}"
ACHOST_DOCKERD_PID="${ACHOST_DOCKERD_PID:-$ACHOST_RUN/dockerd.pid}"
ACHOST_CONTAINERD_PID="${ACHOST_CONTAINERD_PID:-$ACHOST_RUN/containerd.pid}"
ACHOST_CONTAINERD_CONFIG="${ACHOST_CONTAINERD_CONFIG:-$ACHOST_ETC/containerd/config.toml}"
ACHOST_DOCKERD_CONFIG="${ACHOST_DOCKERD_CONFIG:-$ACHOST_RUN/dockerd-daemon.json}"
ACHOST_DOCKERD_LOG="${ACHOST_DOCKERD_LOG:-$ACHOST_LOG_DIR/dockerd.log}"
ACHOST_CONTAINERD_LOG="${ACHOST_CONTAINERD_LOG:-$ACHOST_LOG_DIR/containerd.log}"
ACHOST_SUPERVISE="${ACHOST_SUPERVISE:-$ACHOST_COMMON_BIN/achost-supervise}"
ACHOST_USE_SUPERVISOR="${ACHOST_USE_SUPERVISOR:-1}"
ACHOST_SUPERVISOR_PID="${ACHOST_SUPERVISOR_PID:-$ACHOST_RUN/achost-supervise.pid}"
ACHOST_SUPERVISOR_SOCKET="${ACHOST_SUPERVISOR_SOCKET:-$ACHOST_RUN/achost-supervise.sock}"
ACHOST_SUPERVISOR_LOG="${ACHOST_SUPERVISOR_LOG:-$ACHOST_LOG_DIR/achost-supervise.log}"
ACHOST_DOCKERD_LAUNCH_PID="${ACHOST_DOCKERD_LAUNCH_PID:-$ACHOST_RUN/dockerd-launch.pid}"
DOCKER_CONFIG="${DOCKER_CONFIG:-$ACHOST_ETC/docker}"
DOCKER_CLI_PLUGIN_EXTRA_DIRS="${DOCKER_CLI_PLUGIN_EXTRA_DIRS:-$DOCKER_CONFIG/cli-plugins:$ACHOST/libexec/docker/cli-plugins}"
DOCKER_HOST="${DOCKER_HOST:-unix://$ACHOST_RUN/docker.sock}"
ACHOST_DOCKER_COMPAT_SOCKET="${ACHOST_DOCKER_COMPAT_SOCKET:-/var/run/docker.sock}"
ACHOST_DOCKER_COMPAT_HOST="${ACHOST_DOCKER_COMPAT_HOST:-unix://$ACHOST_DOCKER_COMPAT_SOCKET}"
CONTAINERD_ADDRESS="${CONTAINERD_ADDRESS:-$ACHOST_RUN/containerd.sock}"
ACHOST_EXTERNAL_CONTAINERD="${ACHOST_EXTERNAL_CONTAINERD:-1}"
ACHOST_DNS_SERVERS="${ACHOST_DNS_SERVERS:-1.1.1.1 8.8.8.8}"
CONTAINER_BRIDGE="${CONTAINER_BRIDGE:-${DOCKER_BRIDGE:-docker0}}"
DOCKER_BRIDGE="$CONTAINER_BRIDGE"
PATH="$ACHOST_BIN:$ACHOST_COMMON_BIN:$ACHOST_LXC_BIN:/system/bin:/system/xbin:/vendor/bin:/product/bin:/data/adb/magisk:${PATH:-}"
LD_LIBRARY_PATH="$ACHOST_LXC/lib:$ACHOST/lib:$ACHOST_COMMON/lib:${LD_LIBRARY_PATH:-}"

export ACHOST ACHOST_BASE ACHOST_DOCKER_MODULE ACHOST_LXC_MODULE ACHOST_COMMON ACHOST_COMMON_BIN ACHOST_BIN ACHOST_ETC ACHOST_RUNTIME_CONF ACHOST_MODULE_TARGET ACHOST_RUNTIME_MODE ACHOST_CGROUP_MODE ACHOST_VAR ACHOST_CONFIG ACHOST_BIND_PATHS ACHOST_RUN ACHOST_LOG_DIR
export ACHOST_LXC ACHOST_LXC_BIN ACHOST_LXC_ETC ACHOST_LXC_VAR ACHOST_LXC_RUN ACHOST_LXC_LOG ACHOST_LXC_ROOTFS ACHOST_LXC_CONTAINERS LXC_BRIDGE LXC_SUBNET ACHOST_CHROOT ACHOST_CHROOT_LAUNCH_MODE ACHOST_NATIVE_ROOT ACHOST_USE_CHROOT ACHOST_DOCKER_ROOT ACHOST_DOCKER_EXEC_ROOT
export ACHOST_CONTAINERD_ROOT ACHOST_CONTAINERD_STATE ACHOST_DOCKERD_PID ACHOST_CONTAINERD_PID
export ACHOST_CONTAINERD_CONFIG ACHOST_DOCKERD_CONFIG ACHOST_DOCKERD_LOG ACHOST_CONTAINERD_LOG ACHOST_SUPERVISE ACHOST_USE_SUPERVISOR
export ACHOST_SUPERVISOR_PID ACHOST_SUPERVISOR_SOCKET ACHOST_SUPERVISOR_LOG ACHOST_DOCKERD_LAUNCH_PID DOCKER_CONFIG DOCKER_CLI_PLUGIN_EXTRA_DIRS
export DOCKER_HOST ACHOST_DOCKER_COMPAT_SOCKET ACHOST_DOCKER_COMPAT_HOST CONTAINERD_ADDRESS ACHOST_EXTERNAL_CONTAINERD ACHOST_DNS_SERVERS CONTAINER_BRIDGE DOCKER_BRIDGE PATH LD_LIBRARY_PATH
